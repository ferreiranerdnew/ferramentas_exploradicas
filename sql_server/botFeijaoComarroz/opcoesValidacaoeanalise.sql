DATA_INICIO varchar(8),
PAPEL VARCHAR(255),
QUANTIDADE INT,
VALOR_MEDIO FLOAT,
STRIKE FLOAT,
T_PUTCALL VARCHAR(5),
STATUS_VENDA CHAR(1),
STATUS_COMPRA CHAR(1),
DATA_VENCIMENTO [varchar](8)
);

